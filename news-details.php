                        <h4 class="title">
                            Why are Children with Cancer in S. Africa Dying in Unbearable Pain?
                        </h4>
                        <p>I had a patient last year, a four-year-old boy, with a type of cancer called neuroblastoma. After initial treatment, the cancer came back, growing in the confined space between his spine and stomach, causing unbearable pain. His mother was a police officer; a strong, stoical woman who had seen a lot, but she was struggling. Many children in South Africa are in pain because they do cannot access palliative care medicines.</p>
                        <p>After several weeks, using every available drug, including numbing him from the waist down, I managed to get the pain under control, and he died a peaceful death.</p>
                        <p>A month ago, a colleague in Durban asked for advice on managing a similar case. She didn’t have access to half the drugs I had (at Red Cross Hospital in Cape Town) and the nurses were refusing to administer even those that were available because they hadn’t been trained how to use them. The child died in excruciating pain.</p>
                        <p><i>The difference between my patient’s relatively good death and the nightmare one of the Durban child is access to proper pain relief within an effective paediatric palliative care system.</i></p>
                        <p>There are only a few places in South Africa offering proper pain relief, despite the fact that at least a million children are dying from – or living with – incurable conditions. Frustratingly, efforts to remedy this unacceptable situation have ground to a halt.</p>
                        <p>In 2010, the Hospice Palliative Care Association of South Africa (HPCA) initiated an alliance that hospices, professionals working in palliative care, academics and some officials from the Department of Health. A draft policy was created but never implemented.</p>
                        <h3>Plan Never Implemented</h3>
                        <p>A turning point came in 2014 when South Africa co-sponsored a resolution at the World Health Assembly in Geneva which called on all member countries to integrate palliative care into the hospitals and community care structures. A section of the resolution highlighted the need for policy-making that was tailored towards the needs of children.</p>
                        <p>We heard nothing from government until July 2016 when the Health Minister, Aaron Motsaoledi, appointed a 12-member steering committee to develop a palliative care policy. I was invited to represent paediatric palliative care. The policy was approved by the National Health Council in 2017 and by 2018, we had developed an implementation plan. It was quite an ambitious plan.</p>
